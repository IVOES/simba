#include "simba.h"

static char stat[128];
static char buf[32];

int main()
{
    struct uart_driver_t uart;
    uint8_t flags = 3;

    sys_start();
    uart_module_init();

    uart_init(&uart, &uart_device[0], 38400, buf, sizeof(buf));
    uart_start(&uart);

    uart_write(&uart, stat, sprintf(stat, "stat: %x", flags));

    while (1);

    uart_stop(&uart);

    return (0);
}
